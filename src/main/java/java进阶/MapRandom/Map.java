package java进阶.MapRandom;

public class Map {
    public static void main(String[] args) {
        //常用属性
        System.out.println(Math.PI); //3.141592653589793
        //常用方法
        System.out.println("随机数"+Math.random()); //[0.0,1.0)
        System.out.println("绝对值"+Math.abs(-52));//52
        System.out.println("向上取值"+Math.ceil(9.4));
        System.out.println("向下取值"+Math.floor(9.9));
        System.out.println("四舍五入"+Math.round(5.4));
        System.out.println("取最大值"+Math.max(45,100));
        System.out.println("取最小值"+Math.min(45,100));

    }
}
